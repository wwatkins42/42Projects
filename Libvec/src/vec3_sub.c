#include "libvec.h"

t_vec3	vec3_sub(t_vec3 va, t_vec3 vb)
{
	t_vec3	v;

	v.x = va.x - vb.x;
	v.y = va.y - vb.y;
	v.z = va.z - vb.z;
	return (v);
}
