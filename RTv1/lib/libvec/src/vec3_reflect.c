#include "libvec.h"

t_vec3	vec3_reflect(t_vec3 v, t_vec3 n)
{
	t_vec3	r;

	r = vec3_fmul(n, -2.0 * vec3_dot(v, n));
	r = vec3_add(r, v);
	return (r);
}
